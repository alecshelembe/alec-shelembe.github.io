			<h3 id="blood groups">Blood groups</h3>
			<p>There are 4 main blood groups (types of blood) – A, B, AB and O. Your blood group is determined by the genes you inherit from your parents.

			Each group can be either RhD positive or RhD negative, which means in total there are 8 blood groups.</p>

			<h3>Antibodies and antigens</h3>
			<p>Blood is made up of red blood cells, white blood cells and platelets in a liquid called plasma. Your blood group is identified by antibodies and antigens in the blood.

				Antibodies are proteins found in plasma. They're part of your body's natural defences. They recognise foreign substances, such as germs, and alert your immune system, which destroys them.

			Antigens are protein molecules found on the surface of red blood cells.</p>

			<h3 id="about blood">About blood</h3>
			<p>There are 4 main blood groups defined by the ABO system:

				blood group A – has A antigens on the red blood cells with anti-B antibodies in the plasma
				blood group B – has B antigens with anti-A antibodies in the plasma
				blood group O – has no antigens, but both anti-A and anti-B antibodies in the plasma
				blood group AB – has both A and B antigens, but no antibodies
				Blood group O is the most common blood group. Almost half of the UK population (48%) has blood group O.

				Receiving blood from the wrong ABO group can be life threatening. For example, if someone with group B blood is given group A blood, their anti-A antibodies will attack the group A cells.

				This is why group A blood must never be given to someone who has group B blood and vice versa.

				As group O red blood cells do not have any A or B antigens, it can safely be given to any other group.

			The NHS Blood and Transplant (NHSBT) website has more information about the different blood groups.</p>

